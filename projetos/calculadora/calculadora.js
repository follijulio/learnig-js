function soma(valor1, valor2) {
  return valor1 + valor2;
}

function sub(valor1, valor2) {
  return valor1 - valor2;
}

function mult(valor1, valor2) {
  return valor1 * valor2;
}

function div(valor1, valor2) {
  return valor1 / valor2;
}

console.log(soma(9, 5));
console.log(sub(8, 2));
console.log(mult(5, 5));
console.log(div(100 , 10));
